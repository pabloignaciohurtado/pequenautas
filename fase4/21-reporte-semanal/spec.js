    if(!canShare()) return Promise.resolve(false);
    var lang = (typeof S==='object' && S) ? S.lang : 'es';
    var t = UI[lang] || UI.es;
    return navigator.share({ title: t.wrTitle+' · '+p.name, text: buildText(p, lang) })
