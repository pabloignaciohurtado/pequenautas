  /* ---------- sellado de timestamp (wrap de window.logRound) ---------- */
  function wrStampLast(){
    var p = (typeof currentProfile==='function') ? currentProfile() : null;
    if(!p || !p.ev || !p.ev.length) return;
    var last = p.ev[p.ev.length-1];
    if(last && last.ts==null){ last.ts = Date.now(); if(typeof saveDB==='function') saveDB(); }
  }
