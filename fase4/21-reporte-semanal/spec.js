  /* ---------- ventana semanal + agregación (paralela a aggregate(), acotada) ---------- */
  var DAY_MS = 24*60*60*1000;
  function weekRange(atMs){
    atMs = (typeof atMs==='number') ? atMs : Date.now();
    return { start: atMs - 6*DAY_MS, end: atMs }; // últimos 7 días, hoy incluido
  }
  function weeklyEvents(p){
    if(!p || !p.ev) return [];
    var r = weekRange();
    return p.ev.filter(function(e){ return typeof e.ts==='number' && e.ts>=r.start && e.ts<=r.end; });
  }
  function weeklyAggregate(p){
    var ev = weeklyEvents(p);
    var rounds = ev.length;
    var first = ev.filter(function(e){ return e.ft; }).length;
    var avg = rounds ? ev.reduce(function(s,e){ return s+e.ms; },0)/rounds : 0;
    var byGame = { math:{r:0,err:0}, reading:{r:0,err:0}, science:{r:0,err:0} };
    var fails = {};
    ev.forEach(function(e){
      var g = byGame[e.g]; if(g){ g.r++; if(e.at>1 || e.as) g.err++; }
      if(e.at>1){ fails[e.k] = (fails[e.k]||0) + (e.at-1); }
    });
    var topFails = Object.keys(fails).map(function(k){ return {k:k,c:fails[k]}; })
      .sort(function(a,b){ return b.c-a.c; }).slice(0,3);
    /* Cada evento de profile.ev sólo se registra en afterCorrect() (ronda
       acertada), así que rounds===estrellas ganadas esta semana. */
    return { rounds:rounds, firstRate: rounds?first/rounds:0, avg:avg, byGame:byGame, topFails:topFails, stars:rounds };
  }
  function faceOf(k){
    if(typeof eduFaceOf==='function'){ try{ return eduFaceOf(k); }catch(e){} }
    return k;
  }
  function fmtDate(ts, lang){
    try{ return new Date(ts).toLocaleDateString(lang==='es'?'es-ES':'en-US', {day:'2-digit', month:'short'}); }
    catch(e){ return ''; }
  }
  function esc(s){
    if(typeof eduEsc==='function') return eduEsc(s);
    return String(s==null?'':s).replace(/[&<>"]/g,function(c){ return {'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;'}[c]; });
  }

  /* ---------- construcción del informe (texto plano) ---------- */
  function buildText(p, lang){
    var t = UI[lang] || UI.es;
    var r = weekRange();
    var a = weeklyAggregate(p);
    var lines = [];
    lines.push(t.wrTitle + ' — ' + p.name);
    lines.push(t.wrPeriod.replace('{a}', fmtDate(r.start, lang)).replace('{b}', fmtDate(r.end, lang)));
    lines.push('');
    if(a.rounds===0){
      lines.push(t.wrNoData);
    } else {
      lines.push('⭐ ' + t.wrStarsWeek + ': ' + a.stars);
      lines.push('🔁 ' + t.wrRoundsWeek + ': ' + a.rounds);
      lines.push('🎯 ' + t.stFirst + ': ' + Math.round(a.firstRate*100) + '%');
      lines.push('⏱ ' + t.stTime + ': ' + (a.avg/1000).toFixed(1) + 's');
      lines.push('');
      lines.push(t.wrByGame + ':');
      ['math','reading','science'].forEach(function(g){
        var gg = a.byGame[g]; if(gg.r>0){
          var acc = Math.round((1-gg.err/gg.r)*100);
          var nm = g==='math'?t.math:(g==='reading'?t.read:t.sci);
          lines.push('  - ' + nm + ': ' + gg.r + ' · ' + acc + '% ' + t.mAcc.toLowerCase());
        }
      });
      if(a.topFails.length){
        lines.push('');
        lines.push(t.stFocus + ':');
        a.topFails.forEach(function(f){ lines.push('  - ' + faceOf(f.k) + ' (' + f.c + '✗)'); });
      }
    }
    lines.push('');
    lines.push(t.wrFooter.replace('{date}', new Date().toLocaleDateString(lang==='es'?'es-ES':'en-US')));
    return lines.join('\n');
  }

  /* ---------- construcción del informe (HTML autocontenido, sin red) ---------- */
  function buildHTML(p, lang){
    var t = UI[lang] || UI.es;
    var r = weekRange();
    var a = weeklyAggregate(p);
    function stat(icon, n, label){
      return '<div class="stat"><div class="n">' + icon + ' ' + esc(String(n)) + '</div><div class="l">' + esc(label) + '</div></div>';
    }
    var body;
    if(a.rounds===0){
      body = '<p class="empty">' + esc(t.wrNoData) + '</p>';
    } else {
      var bars = '';
      ['math','reading','science'].forEach(function(g){
        var gg = a.byGame[g]; if(gg.r>0){
          var acc = Math.round((1-gg.err/gg.r)*100);
          var nm = g==='math'?t.math:(g==='reading'?t.read:t.sci);
          var col = g==='math'?'#4EA8DE':(g==='reading'?'#F2748C':'#57C596');
          bars += '<div class="bar"><div class="lab"><span>' + esc(nm) + '</span><span>' + acc + '%</span></div>' +
            '<div class="track"><div class="fill" style="width:' + acc + '%;background:' + col + '"></div></div></div>';
        }
      });
      var fails = '';
      if(a.topFails.length){
        fails = '<h3>' + esc(t.stFocus) + '</h3><ul class="fails">' +
          a.topFails.map(function(f){ return '<li>' + esc(faceOf(f.k)) + ' <span class="c">' + f.c + ' ✗</span></li>'; }).join('') +
          '</ul>';
      }
      body = '<div class="stats">' + stat('⭐', a.stars, t.wrStarsWeek) + stat('🔁', a.rounds, t.wrRoundsWeek) +
        stat('🎯', Math.round(a.firstRate*100)+'%', t.stFirst) + stat('⏱', (a.avg/1000).toFixed(1)+'s', t.stTime) + '</div>' +
        (bars ? ('<h3>' + esc(t.wrByGame) + '</h3>' + bars) : '') + fails;
    }
    return '<!DOCTYPE html><html lang="' + lang + '"><head><meta charset="UTF-8">' +
      '<meta name="viewport" content="width=device-width, initial-scale=1.0">' +
      '<title>' + esc(t.wrTitle) + ' — ' + esc(p.name) + '</title>' +
      '<style>' +
        'body{font-family:system-ui,-apple-system,"Segoe UI",sans-serif;background:#FDF6EC;color:#3B3355;margin:0;padding:28px;}' +
        '.card{max-width:560px;margin:0 auto;background:#fff;border-radius:24px;box-shadow:0 10px 30px rgba(59,51,85,.12);padding:26px;}' +
        'h1{font-size:22px;margin:0 0 4px;}.sub{color:#6C6685;font-size:14px;margin:0 0 18px;}' +
        '.stats{display:grid;grid-template-columns:1fr 1fr;gap:10px;margin-bottom:12px;}' +
        '.stat{background:#F8F6FD;border-radius:14px;padding:12px;}.stat .n{font-size:19px;font-weight:700;}.stat .l{font-size:12px;color:#6C6685;}' +
        'h3{font-size:13px;color:#6C6685;margin:16px 0 6px;text-transform:uppercase;letter-spacing:.03em;}' +
        '.bar{margin-top:8px;}.bar .lab{display:flex;justify-content:space-between;font-size:13px;font-weight:600;margin-bottom:4px;}' +
        '.bar .track{height:10px;border-radius:6px;background:#EEE9F6;overflow:hidden;}.bar .fill{height:100%;border-radius:6px;}' +
        '.fails{list-style:none;padding:0;margin:0;}.fails li{display:flex;justify-content:space-between;padding:6px 0;border-top:1px solid #F0ECF6;font-size:14px;}' +
        '.fails .c{color:#F2748C;font-weight:600;}.empty{color:#6C6685;font-size:14px;}' +
        'footer{margin-top:18px;font-size:11.5px;color:#9992ac;text-align:center;}' +
      '</style></head><body><div class="card">' +
      '<h1>' + esc(t.wrTitle) + ' · ' + esc(p.avatar) + ' ' + esc(p.name) + '</h1>' +
      '<p class="sub">' + esc(t.wrPeriod.replace('{a}', fmtDate(r.start, lang)).replace('{b}', fmtDate(r.end, lang))) + '</p>' +
      body +
      '<footer>' + esc(t.wrFooter.replace('{date}', new Date().toLocaleDateString(lang==='es'?'es-ES':'en-US'))) + '</footer>' +
      '</div></body></html>';
  }

  function slug(s){
    return String(s||'peque').normalize('NFD').replace(/[\\u0300-\\u036f]/g,'')
      .replace(/[^a-zA-Z0-9]+/g,'-').toLowerCase().replace(/(^-|-$)/g,'') || 'peque';
  }
  function triggerDownload(filename, content, mime){
    try{
      var blob = new Blob([content], {type: mime});
      var url = URL.createObjectURL(blob);
      var a = document.createElement('a'); a.href=url; a.download=filename;
      document.body.appendChild(a); a.click(); a.remove();
      setTimeout(function(){ URL.revokeObjectURL(url); }, 1000);
      return true;
    }catch(e){ return false; }
  }
  function downloadHTML(p){
    p = p || (typeof currentProfile==='function' ? currentProfile() : null); if(!p) return false;
    var lang = (typeof S==='object' && S) ? S.lang : 'es';
    return triggerDownload('pequenautas-informe-'+slug(p.name)+'.html', buildHTML(p, lang), 'text/html;charset=utf-8');
  }
  function downloadText(p){
    p = p || (typeof currentProfile==='function' ? currentProfile() : null); if(!p) return false;
    var lang = (typeof S==='object' && S) ? S.lang : 'es';
    return triggerDownload('pequenautas-informe-'+slug(p.name)+'.txt', buildText(p, lang), 'text/plain;charset=utf-8');
  }
  function canShare(){ return typeof navigator!=='undefined' && typeof navigator.share==='function'; }
